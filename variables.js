//! var keyword
var a;  //declaration is possible
a=10;   //initialization is possible
console.log(a);
var a;  //re-declaration is possible
a=20;   //re-initialization is possible
console.log(a);

//! ley keyword
let b;  //declaration is possible
b=20;   //initialization is possible
console.log(b);
// let b;  //re-declaration is not possible
b=200;  //re-initialization is possible
console.log(b);


//!const keyword
// const c; 
// c=30;
const c=200; //declaration , inititalization is possible in same line only
console.log(c);

